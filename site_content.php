<div class="article">
<h2>Для чего нужна CSS?</h2>
<p>
CSS используется для определения стилей ваших документов, в том числе дизайна, верстки и вариаций макета для различных устройств и размеров экрана. Вы можете разместить стили CSS внутри тега "HEAD" документа с встроенной таблицей стилей, или приложить отдельный CSS-файл, который будет определять ваши стили извне.
</p>
</div>

<div class="article">
<h2>JavaScript это...</h2>
<p>
JavaScript ("JS" для краткости) — это полноценный динамический язык программирования, который применяется к HTML документу, и может обеспечить динамическую интерактивность на веб-сайтах. Его разработал Brendan Eich, сооснователь проекта Mozilla, Mozilla Foundation и Mozilla Corporation.
</p>

